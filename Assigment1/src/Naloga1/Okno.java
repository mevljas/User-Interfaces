    private void izvediActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_izvediActionPerformed
        if (dodaj.isSelected()) {
            String vneseno = vnos.getText();
            if (!vneseno.isEmpty()) {
                if (!dvojniki.isSelected() || ((DefaultComboBoxModel)kraj.getModel()).getIndexOf(vneseno) == -1) {
                    kraj.addItem(vneseno);
                    sporocila.setText("Dodajam");
                }

            }

        }
        else if (kraj.getItemCount() != 0) {
            if (odstraniPrvega.isSelected()) {
                kraj.removeItemAt(0);
                sporocila.setText("Odstranjujem prvega");
            }
            else if (odstraniIzbranega.isSelected()) {
                kraj.removeItemAt(kraj.getSelectedIndex());
                sporocila.setText("Odstranjujem izbranega");
            }
        }
        {

        }
    }//GEN-LAST:event_izvediActionPerformed

