    private void prikaziElement(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_prikaziElement
        int izbrani = Integer.parseInt(izbraniIndeks.getValue().toString());

        if (kraj.getItemCount() > izbrani) {
            sporocila.setText(kraj.getItemAt(izbrani));
        }
        else{
            sporocila.setText("Ni elementa");
        }
    }//GEN-LAST:event_prikaziElement

