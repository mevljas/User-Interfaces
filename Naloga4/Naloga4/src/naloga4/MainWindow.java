    private boolean checkDataNarocilo() {
        boolean error = false;
        statusnaVrstica.setForeground(Color.BLACK);
        LineBorder redBorder = new LineBorder(Color.RED, 2);
        Border defaultBorder = new JTextField().getBorder();

        if (datumIzposojePicker.getDate() == null || datumIzposojePicker.getDate().after(datumVrnitvePicker1.getDate())) {
            datumIzposojePicker.setBorder(redBorder);
            datumIzposojeLabel.setForeground(Color.RED);
            error = true;
        } else {
            datumIzposojePicker.setBorder(null);
            datumIzposojeLabel.setForeground(Color.BLACK);
        }
        if (datumVrnitvePicker1.getDate() == null || datumIzposojePicker.getDate().after(datumVrnitvePicker1.getDate())) {
            datumVrnitvePicker1.setBorder(redBorder);
            datumVrnitveLabel1.setForeground(Color.RED);
            error = true;
        } else {
            datumVrnitvePicker1.setBorder(null);
            datumVrnitveLabel1.setForeground(Color.BLACK);
        }

        if (datumIzposojePicker.getDate() != null && datumVrnitvePicker1.getDate() != null) {
            Long datumIzposoje = (Long) datumIzposojePicker.getDate().getTime() / 10000000;
            Long datumVrnitve = (Long) datumVrnitvePicker1.getDate().getTime() / 10000000;

            if (datumIzposoje > datumVrnitve) {
                datumIzposojePicker.setBorder(redBorder);
                datumIzposojeLabel.setForeground(Color.RED);
                datumVrnitvePicker1.setBorder(redBorder);
                datumVrnitveLabel1.setForeground(Color.RED);
                error = true;
            } else {
                datumIzposojePicker.setBorder(null);
                datumIzposojeLabel.setForeground(Color.BLACK);
                datumVrnitvePicker1.setBorder(null);
                datumVrnitveLabel1.setForeground(Color.BLACK);
            }

            if (datumIzposoje.equals(datumVrnitve)) {
                if (uraIzposojeComboBox.getSelectedIndex() >= uraVrnitveComboBox1.getSelectedIndex()) {
                    uraIzposojeComboBox.setBorder(redBorder);
                    uraIzposojeLabel.setForeground(Color.RED);
                    uraVrnitveComboBox1.setBorder(redBorder);
                    uraVrnitveLabel1.setForeground(Color.RED);
                    error = true;
                } else {
                    uraIzposojeComboBox.setBorder(null);
                    uraIzposojeLabel.setForeground(Color.BLACK);
                    uraVrnitveComboBox1.setBorder(null);
                    uraVrnitveLabel1.setForeground(Color.BLACK);
                }
            }

        }

        if (error) {
            statusnaVrstica.setForeground(Color.RED);
            statusnaVrstica.setText("* Pozor: Pred shranjevanjem je potrebno pravilno izpolniti vsa polja.");
        } else {
            statusnaVrstica.setText("Aplikacija pripavljena na uporabo.");
            resetBorders();
        }

        return error;

    }

