    private void setCarPrice() {
        if (datumVrnitvePicker1.getDate() != null && datumIzposojePicker.getDate() != null) {
            cenaAvtaDan = 50 + velikostAvtomobilajComboBox1.getSelectedIndex() * 50 + avtomobiljComboBox1.getSelectedIndex() * 10;
            cenaAvtaDan = avtomatskiMenjalnik.isSelected() ? cenaAvtaDan + 5 : cenaAvtaDan;
            cenaAvtaDan = dizeljRadioButton1.isSelected() ? cenaAvtaDan + 5 : cenaAvtaDan;

            int days = (int) ((datumVrnitvePicker1.getDate().getTime() - datumIzposojePicker.getDate().getTime()) / (1000 * 60 * 60 * 24));
            int hours = uraVrnitveComboBox1.getSelectedIndex() - uraIzposojeComboBox.getSelectedIndex();
            cenaAvtaSkupaj = days * cenaAvtaDan + hours * (cenaAvtaDan / 24);
        } else {
            cenaAvtaDan = 0;
            cenaAvtaSkupaj = 0;
            znesekAvta = 0;
        }
        cenaAvtomobila.setText(cenaAvtaDan + " €");
        cenaSkupaj.setText(cenaAvtaSkupaj + " €");
    }

    private void setFinalCarPrice() {
        znesekAvta = cenaAvtaSkupaj;
        if (zavarovanjejCheckBox1.isSelected()) {
            znesekAvta += 2;

        }
        znesek.setText(znesekAvta + " €");
    }

